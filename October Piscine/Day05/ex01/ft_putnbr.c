void	ft_putchar(char c);

void	ft_putnbr(int nb)

{
	if (nb == -2147483648)
	{
		ft_putchar('-');
		ft_putnbr(214);
		nb = 7483648;
	}

	if (nb < 0)
	{
		ft_putchar('-');
		nb *= -1;
	} 
	if (nb <= 9)
	{
		ft_putchar(nb + 48);
	}
	if(nb > 9)
	{
		ft_putnbr(nb/10);
		ft_putnbr(nb % 10);
	}
}
