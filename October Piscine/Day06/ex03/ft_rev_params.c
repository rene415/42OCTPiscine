#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstring(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		ft_putchar(str[i]);
		i++;
	}
}

int		main(int argc, char **argv)
{
	int enzo;

	enzo = (argc - 1);
	while (enzo != '\0')
	{
		ft_putstring(argv[enzo]);
		ft_putchar('\n');
		enzo--;
	}
	return (0);
}
